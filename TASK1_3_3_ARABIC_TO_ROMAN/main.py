# Создадим словарь, хранящий пары римское число - арабское число)
romans = dict(I=1, V=5, X=10, L=50, C=100, D=500, M=1000)

# XIV


def roman_to_int(roman):
    result = 0  # итоговое число
    for i, c in enumerate(roman):
        # случай, когда предыдущее римское число меньше следующего
        if i+1 < len(roman) and romans[roman[i]] < romans[roman[i+1]]:
            result -= romans[roman[i]]  # вычитаем из результата текущее число
        else:  # если следующее римское число больше предыдущего
            result += romans[roman[i]]
    return result


